// 1) * Написать программу которая спрашивает у пользователя 2 числа(через prompt) и выводит в консоль какое меньше.Если числа равны, то вывести что они равны.Например: 5 и 10 "5 меньше 10", 7 и 1 "7 больше 1", 5 и 5 "числа равны"

let userNumberOne = +prompt('Напишите 1 число');
let userNumberTwo = +prompt('Напишите 2 число');

if (userNumberOne < userNumberTwo) {
  console.log('Первое число меньне второго числа');
} else if (userNumberOne > userNumberTwo) {
  console.log('Первое число больше второго');
} else {
  console.log('Числа равны');
}

//2) * Написать программу "Треугольник в цикле".Программа должна в консоль нарисовать следующую фигуру:

// максимальная строка
let MaxString = '7';
// 1-й цикл до 7 строк с числами
for (i = 0; i <= MaxString; i++) {
  let result = '';
  // 2-й цикл добавляем звезду в каждой строке поэтапно прибавляем по одной и прибавляем
  for (counter = 0; counter < i; counter++) {
    result += '*';
  }
  console.log(result);
}

