for (let i = 1; i <= 100; i++) {
  string = '';

  // если кратно 3, то добавляем Fizz
  if (i % 3 === 0) {
    string += 'Fizz';
  }

  // если кратно 5, то добавляем Buzz
  if (i % 5 === 0) {
    string += 'Buzz';
  }

  // если кратно 3 и 5, то результат - FizzBuzz
  console.log(string ? string : i);
}

